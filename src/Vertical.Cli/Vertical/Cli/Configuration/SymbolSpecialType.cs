namespace Vertical.Cli.Configuration;

/// <summary>
/// Defines symbol special types.
/// </summary>
public enum SymbolSpecialType
{
    /// <summary>
    /// Indicates no special designation.
    /// </summary>
    None,
    
    /// <summary>
    /// Indicates the help option.
    /// </summary>
    HelpOption,
    
    /// <summary>
    /// Indicates a response file option.
    /// </summary>
    ResponseFileOption
}